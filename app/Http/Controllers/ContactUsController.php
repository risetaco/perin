     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //validate form
        $this->validate($request, [
            'name'     => 'required',
            'email'     => 'required|email',
            'phone'   => 'required',
            'title'   => 'required',
            'content'   => 'required'
        ]);

        $mailData = [
            'name'     => $request->name,
            'email'     => $request->email,
            'phone'   => $request->phone,
            'title'   => $request->title,
            'content'   => $request->content
        ];


        Mail::to('kemenperin.maritim@gmail.com')->cc('sarifahulfa@kemenperin.go.id')->send(new ContactUs($mailData));
        return Inertia::render('KontakKami', [ 'message' => 'Sukses mengirim pesan']);
    }

